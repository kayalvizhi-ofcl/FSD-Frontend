//Array destructing
const numbers = [10, 20, 30];

// Extract values
const [a, b, c] = numbers;

console.log(a); 
console.log(b); 
console.log(c); 

//Object destructing
const person = {
  name: "Kayal",
  age: 21,
  city: "Chennai"
};

// Extract values
const { name, age, city } = person;

console.log(name); 
console.log(age);  
console.log(city); 

